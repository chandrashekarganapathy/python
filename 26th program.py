# calculator
x=100
y=200
def add(x,y):
    return x+y
def sub(x,y):
    return x-y
def multiply(x,y):
    return x*y
def div(x,y):
    return x/y
def float(x,y):
    return x//y
def mod(x,y):
    return x%y
print(add(x,y))
print(sub(x,y))
print(multiply(x,y))
print(div(x,y))
print(float(x,y))
print(mod(x,y))
